# Plays a single round of the game
def play_round():
    pools = init_pools()
    player_turn = ask_who_starts()

    while True:
        display_pools(pools)

        if player_turn:
            print("\nğŸ® Your turn:")
            player_move(pools)
            if is_game_over(pools):
                print("\nğŸ‰ You took the last stone. You win this round!")
                return "Player"
        else:
            print("\nğŸ¤– Computer's turn:")
            time.sleep(1)
            computer_move(pools)
            if is_game_over(pools):
                print("\nğŸ’» Computer took the last stone. Computer wins this round.")
                return "Computer"

        player_turn = not player_turn
        print("\n" + "-" * 50)

